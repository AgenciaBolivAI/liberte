// Reads the Month-2 (days 21-40) content-workflow journal (per-agent structured
// results) and emits src/data/month2.ts — the code data module that feeds the
// DayPage lesson wrappers + the tutor for weeks 5-8. Pass the journal.jsonl path.
// Deterministic: each generation agent returns its own `day` (21-40); the verify
// agents return {day, approved, issues} (no `vocabulary`) and are ignored here.
import { readFileSync, writeFileSync } from "node:fs";

const journal = process.argv[2];
if (!journal) {
  console.error("usage: node scripts/gen-month2-data.mjs <path/to/journal.jsonl>");
  process.exit(1);
}
// Days 7-20 share this Gym cérébral M1 video; reuse it for 21-40 (same as WEEK34).
const GYM = "https://www.youtube.com/embed/AdJPOTR-CdU";

// Collect every JSON object in the journal, unwrap {type:"result",result} or raw,
// and keep only full day-content payloads (have 30 vocabulary items + a valid day).
const objs = readFileSync(journal, "utf8")
  .trim()
  .split("\n")
  .filter(Boolean)
  .map((l) => {
    try { return JSON.parse(l); } catch { return null; }
  })
  .filter(Boolean);

const contents = [];
for (const o of objs) {
  const r = o && typeof o === "object" && o.result !== undefined ? o.result : o;
  if (r && Array.isArray(r.vocabulary) && r.vocabulary.length === 30 && Number.isInteger(r.day) && r.day >= 21 && r.day <= 40) {
    contents.push(r);
  }
}

const MONTH2 = {};
for (const r of contents) {
  const key = String(r.day);
  if (MONTH2[key]) {
    console.error("DUPLICATE full-content result for day", r.day);
    process.exit(1);
  }
  MONTH2[key] = {
    gym: GYM,
    vocabulary: r.vocabulary,
    flashQuiz: r.flashQuiz,
    grammar: r.grammarStructures,
    vocabGames: {
      reading: r.vocabReading,
      listening: r.vocabListening,
      speaking: r.vocabSpeaking,
      writing: r.vocabWriting,
    },
    clesReading: r.clesReading,
    clesGames: {
      listening: r.clesListening,
      speaking: r.clesSpeaking,
      writing: r.clesWriting,
    },
    defiSteps: r.defiSteps,
    defiCriteria: r.defiCriteria,
    tutor: {
      role: r.tutorRole,
      opener_fr: r.tutorOpenerFr,
      opener_es: r.tutorOpenerEs,
      objectives: (r.tutorObjectives || []).slice(0, 3), // model filter caps at 3
      topic: r.tutorTopic,
    },
  };
}

const days = Object.keys(MONTH2).map(Number).sort((a, b) => a - b);
const want = Array.from({ length: 20 }, (_, i) => 21 + i);
if (days.join(",") !== want.join(",")) {
  console.error("MISSING/EXTRA days — got:", days.join(",") || "(none)");
  process.exit(1);
}

const header = `// AUTO-GENERATED by scripts/gen-month2-data.mjs from the days 21-40 content workflow.
// Source: the client's Mes 2 "JE COMPRENDS" Mapa/Diccionario, transcribed +
// generated by scripts/gen-month2.wf.mjs (the content workflow, kept for provenance).
// Shape identical to WEEK34 (src/data/week34.ts) — reuses the WeekDay type.
// To change content, edit the source + regenerate; do not hand-edit this file.
import type { WeekDay } from "./week34";

export const MONTH2: Record<string, WeekDay> = `;

writeFileSync("src/data/month2.ts", header + JSON.stringify(MONTH2, null, 2) + ";\n");
console.log("Wrote src/data/month2.ts for days", days.join(","), "— sizes:", days.map((d) => `${d}:${MONTH2[String(d)].vocabulary.length}v`).join(" "));
