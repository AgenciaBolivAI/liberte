// Emits supabase/migrations/20260725000000_seed_month2_rich.sql — inserts days
// 21-40 (Month 2 "JE COMPRENDS", weeks 5-8) into authored_days as PUBLISHED
// "rich" lessons (MONTH2 content + MONTH2_META meta), so weeks 5-8 are
// teacher-editable AND render in the real weeks-1-2 design. Idempotent: deletes
// 21-40 first. Run: node scripts/gen-month2-seed.mjs
import { readFileSync, writeFileSync } from "node:fs";
import ts from "typescript";

// MONTH2 — the generated content module is valid JSON after the `= `.
const m2src = readFileSync("src/data/month2.ts", "utf8");
const jsonStart = m2src.indexOf("= {", m2src.indexOf("export const MONTH2"));
const MONTH2 = JSON.parse(m2src.slice(jsonStart + 2).replace(/;\s*$/, "").trim());

// MONTH2_META — transpile the TS module to CommonJS and eval it (same trick the
// week34 seed uses); it has only a stripped `import type`, no runtime imports.
const metaSrc = ts.transpileModule(readFileSync("src/data/month2.meta.ts", "utf8"), {
  compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2022 },
}).outputText;
const mm = {};
new Function("exports", "module", metaSrc)(mm, { exports: mm });
const MONTH2_META = mm.MONTH2_META;

const days = Object.keys(MONTH2).map(Number).sort((a, b) => a - b);
const want = Array.from({ length: 20 }, (_, i) => 21 + i);
if (days.join(",") !== want.join(",")) {
  console.error("MONTH2 must cover days 21-40, got:", days.join(","));
  process.exit(1);
}

const sq = (s) => String(s).replace(/'/g, "''"); // single-quote escape for SQL literals

let rows = "";
for (const d of days) {
  const meta = MONTH2_META[String(d)];
  if (!meta) {
    console.error("missing MONTH2_META for day", d);
    process.exit(1);
  }
  const rich = { ...MONTH2[String(d)], meta };
  const json = JSON.stringify(rich);
  if (json.includes("$rich$")) {
    console.error("day", d, "content contains the dollar-quote tag — pick another tag");
    process.exit(1);
  }
  rows +=
    `INSERT INTO public.authored_days (day_id, title, subtitle, status, rich) VALUES (` +
    `${d}, '${sq(meta.label)}', '${sq(meta.clesSub)}', 'published', $rich$${json}$rich$::jsonb);\n`;
}

const sql = `-- AUTO-GENERATED by scripts/gen-month2-seed.mjs — DO NOT EDIT BY HAND.
-- Seeds days 21-40 (Month 2 "JE COMPRENDS") as PUBLISHED rich lessons
-- (teacher-editable from the content manager, rendered through the weeks-1-2
-- shell). Regenerate after editing src/data/month2.ts or src/data/month2.meta.ts.
-- Idempotent.
DELETE FROM public.authored_days WHERE day_id BETWEEN 21 AND 40;
${rows}`;

writeFileSync("supabase/migrations/20260725000000_seed_month2_rich.sql", sql);
console.log("Wrote supabase/migrations/20260725000000_seed_month2_rich.sql for days", days.join(","));
